use std::collections::HashMap;

fn get_mode(list: &[i64]) -> i64 {
    let mut map: HashMap<i64, i64> = HashMap::new();
    for i in list {
        let count = map.entry(*i).or_insert(0);
        *count += 1;
    }
    let mut max_val = 0;
    let mut mode = 0;
    for (key, value) in map {
        if value > max_val {
            max_val = value;
            mode = key;
        }
    }
    mode
}

fn get_median(list: &mut [i64]) -> i64 {
    list.sort_unstable();
    if list.len() % 2 == 0 {
        (list[list.len() / 2 - 1] + list[list.len() / 2]) / 2
    } else {
        list[list.len() / 2 - 1]
    }
}

fn get_mean_10(list: &[i64]) -> i64 {
    let mut running_sum: i64 = 0;
    for i in list {
        running_sum += i;
    }
    running_sum / list.len() as i64
}

fn get_mean(list: &[i64]) -> i64 {
    let mut means: Vec<i64> = Vec::new();
    for i in 0..list.len() / 10 {
        let start: usize = i * 10;
        let end: usize = if ((i + 1) * 10) > list.len() {
            list.len() % 10
        } else {
            (i + 1) * 10
        };
        means.push(get_mean_10(&list[start..end]));
    }
    let mut running_sum: i64 = 0;
    for i in &means {
        running_sum += i;
    }
    running_sum / means.len() as i64
}

